      ->pack(-side => 'left', -expand => 1, -pady => 3);
   $box->Button(-text => 'Cancel', -command => sub { $LoginDialog->withdraw() })
      ->pack(-side => 'right', -expand => 1, -pady => 3);
   $box->pack(-expand => 1, -fill => 'both');
   }
   
